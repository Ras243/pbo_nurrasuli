package luas_bangun_datar;

public class Persegi {
    private int sisi;
    private int luas;

    public int getSisi() {
        return sisi;
    }

    public void setSisi(int sisi) {
        this.sisi = sisi;
    }

    public int getLuas() {
        return luas;
    }

    public void setLuas() {
        this.luas = sisi*sisi;
    }
}
